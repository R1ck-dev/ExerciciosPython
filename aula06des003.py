#Crie um programa que leia dois números e mostre a soma entre eles

# Solicita ao usuário que insira um número e converte para inteiro, armazenando em 'n1'
n1 = int(input('Digite um valor qualquer: '))

# Solicita ao usuário que insira outro número e converte para inteiro, armazenando em 'n2'
n2 = int(input('Digite outro valor qualquer: '))

# Realiza a soma dos dois números e armazena o resultado na variável 'soma'
soma = n1 + n2

# Exibe o resultado da soma na tela
print(f'A soma de {n1} + {n2} é igual a {soma}')
